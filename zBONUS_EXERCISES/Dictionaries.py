# Creating a python dictionary with the information of a friend

#inputing my friends information in a dictionary
Friend = {"first_name" : "Suha",
          "last_name" : "Fatima",
          "Age" : 18,
          "city" : "Dubai"}

#printing each information one by one
print(f"My friends first name is, {Friend['first_name']}")
print(f"My friends last name is, {Friend['last_name']}")
print(f"My friend is {Friend['Age']} years old")
print(f"My Friend lives in {Friend['city']}")